package week7;

public class Class7_7_While_Loop_Increment_By_2_1 {
    public static void main(String[] args) {
        printOddNumbers();
    }

    public static void printOddNumbers() {
        int i = 3;
        while (i<=22){
            System.out.println(i);
            i+=2;
        }
    }
}
